using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class Position2D
{
    public double x;
    public double y;

    /// <summary>
    /// 2D pos: x, y.
    /// </summary>
    /// <param name="x"> x </param>
    /// <param name="y"> y </param>
    public Position2D(double x, double y)
    {
        this.x = x;
        this.y = y;
    }
}
